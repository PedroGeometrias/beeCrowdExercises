#include <stdio.h>
#include <math.h>

double distancia(int x, int y, int p, int q){
    double distancia = sqrt(pow(x - p, 2) + pow(y - q, 2)); 
    return distancia;
}

int main(){
    int x, y, r;
    int p, q, a, t;
    while(1){
        scanf("%d %d %d", &x, &y, &r);
        scanf("%d %d %d %d", &p,  &q, &a, &t);

    }

    return 0;
}
